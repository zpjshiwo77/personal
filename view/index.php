$(".mymove").mouseenter(function(){
	$(this).find("p,b").each(function(){
		var itemx = parseInt($(this)[0].style.left);
		var itemy = parseInt($(this)[0].style.top);
		var font = parseInt($(this)[0].style.fontSize);
		if(font > 25){
			$(this).css("left",itemx + 10);
			$(this).css("top",itemy + 4);
		}
		else if(font == 25){
			$(this).css("left",itemx + 16);
			$(this).css("top",itemy + 2);
		}
		else{
			$(this).css("left",itemx - 8);
			$(this).css("top",itemy - 4);
		}
		if($(this).html() == "宫崎骏动画"){
			$(this).css("left",itemx - 8);
		}
		if($(this).html() == "张学友"){
			$(this).css("top" ,itemy - 8);
			$(this).css("left",itemx - 6);
		}
	});
})
$(".mymove").mouseleave(function(){
	$(this).find("p,b").each(function(){
		$(this).css("left",parseInt($(this).attr('left')));
		$(this).css("top",parseInt($(this).attr('top')));
	});
})

